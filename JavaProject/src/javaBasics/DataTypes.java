package javaBasics;

public class DataTypes {

	//main() - main method is starting/execution point of the program
	public static void main(String[] args) {
		

		//primitive data types are int, double, char, boolean
		
		//1.int 
		int a=10;
		System.out.println(a);
		//2.double
		double d=12.13;
		double d1=100;
		System.out.println(d);
		
		//3.char
		char c='a';
		char b='A';
		
		//4.boolean
		
		boolean b1=true;
		boolean b2=false;
		System.out.println(b1);
		
		//5.String-- String is a Class, not a data type
		
		String S1="This is my first program";
		
		System.out.println(S1);
		
	}

}
